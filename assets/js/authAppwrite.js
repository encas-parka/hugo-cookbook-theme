/**
 * Affiche un √©tat de l'UI et masque les autres.
 * @param {string} state - L'√©tat √† afficher ('loading', 'loggedIn', 'loggedOut', 'emailNotVerified')
 */
function showUIState(state) {
  if (loadingState) loadingState.style.display = (state === 'loading') ? 'block' : 'none';
  const appWriteUserName = localStorage.getItem('appwrite-user-name');
  
  if (state === 'loggedIn') {
    if (loggedInState) loggedInState.style.display = 'block';
    if (loggedOutState) loggedOutState.style.display = 'none';
    if (loggedOutSections) loggedOutSections.style.display = 'none';
    if (emailNotVerifiedState) emailNotVerifiedState.style.display = 'none';
    if (headerLoggedOut) headerLoggedOut.style.display = 'none';
    if (headerLoggedIn) headerLoggedIn.style.display = 'block';

    if (userEncasGmx && appWriteUserName) {
      if (appWriteUserName === "encas-cookbook") {
        userEncasGmx.style.display = 'block';
      } else {
        welcomeUser.textContent = `Bienvenue ${appWriteUserName}`;
      }
    }
  } else if (state === 'loggedOut') {
    if (loggedInState) loggedInState.style.display = 'none';
    if (loggedOutState) loggedOutState.style.display = 'block';
    if (loggedOutSections) loggedOutSections.style.display = 'block';
    if (emailNotVerifiedState) emailNotVerifiedState.style.display = 'none';
    if (headerLoggedOut) headerLoggedOut.style.display = 'block';
    if (headerLoggedIn) headerLoggedIn.style.display = 'none';
  } else if (state === 'emailNotVerified') {
    if (loggedInState) loggedInState.style.display = 'none';
    if (loggedOutState) loggedOutState.style.display = 'none';
    if (loggedOutSections) loggedOutSections.style.display = 'none';
    if (emailNotVerifiedState) emailNotVerifiedState.style.display = 'block';
    if (headerLoggedOut) headerLoggedOut.style.display = 'none';
    if (headerLoggedIn) headerLoggedIn.style.display = 'none';
  }
}



/**
 * Configure l'authentification du CMS en appelant la fonction Appwrite.
 * L√®ve une exception en cas d'erreur.
 */
async function setupCmsAuthentication() {
  // console.log("üîÑ [setupCmsAuthentication] Appel de la fonction Appwrite pour obtenir le token CMS...");

  try {
    // V√©rifier d'abord si l'utilisateur a confirm√© son email
    const account = await getAccount();
    const appwriteUser = await account.get();

    if (!appwriteUser.emailVerification) {
      console.warn("‚ö†Ô∏è [setupCmsAuthentication] Email non v√©rifi√© - impossible d'obtenir le token CMS");
      throw new Error("EMAIL_NOT_VERIFIED");
    }

    const functions = await getFunctions();
    const response = await functions.createExecution(
      APPWRITE_FUNCTION_ID,
      '', // Le corps de la requ√™te est vide
      false
    );

    // console.log("üîç [setupCmsAuthentication] R√©ponse re√ßue - Status:", response.responseStatusCode);
    // console.log("üîç [setupCmsAuthentication] Corps de r√©ponse:", response.responseBody);

    if (response.responseStatusCode !== 200) {
      let serverError = response.responseBody;
      try {
        const parsedBody = JSON.parse(response.responseBody);
        if (parsedBody.error) serverError = parsedBody.error;
      } catch (p_err) { /* ignore */ }
      console.error("‚ùå [setupCmsAuthentication] Erreur de la fonction:", serverError);
      throw new Error(`Erreur de la fonction (${response.responseStatusCode}): ${serverError}`);
    }

    const cmsAuth = JSON.parse(response.responseBody);
    // console.log("üîç [setupCmsAuthentication] Token CMS pars√©:", {
    //   hasToken: !!cmsAuth.token,
    //   tokenLength: cmsAuth.token ? cmsAuth.token.length : 0,
    //   hasId: !!cmsAuth.id,
    //   hasEmail: !!cmsAuth.email,
    //   backendName: cmsAuth.backendName
    // });

    localStorage.setItem('sveltia-cms.user', JSON.stringify(cmsAuth)); // Stocker directement le token CMS
    // console.log("‚úÖ [setupCmsAuthentication] Authentification CMS stock√©e dans localStorage");

    return cmsAuth;
  } catch (error) {
    console.error("‚ùå [setupCmsAuthentication] Erreur lors de la configuration CMS:", error);
    throw error;
  }
}

/**
 * Logique principale ex√©cut√©e au chargement de la page de connexion.
 */
 async function handleLoginPageLoad() {
     showUIState('loading');

     try {
         if (!window.Appwrite) {
             console.error("‚ùå [handleLoginPageLoad] SDK Appwrite non disponible !");
             throw new Error("SDK Appwrite non charg√©");
         }
         // console.log("‚úÖ [handleLoginPageLoad] SDK Appwrite disponible");

         const account = await getAccount();
         const cmsUser = getLocalCmsUser();
         let appwriteUser = null;

         // Debug: Afficher l'√©tat du token CMS
         // console.log("üîç [handleLoginPageLoad] Token CMS brut:", cmsUser);

         // 1. Tenter de r√©cup√©rer la session Appwrite active
         try {
             appwriteUser = await account.get();
             // console.log("‚úÖ [handleLoginPageLoad] Session Appwrite trouv√©e:", appwriteUser.email);
         } catch (e) {
             // console.log("‚ÑπÔ∏è [handleLoginPageLoad] Pas de session Appwrite active:", e.message);
         }

         // 2. V√©rifier si le token CMS est valide (validation plus permissive)
         const isValidCmsUser = cmsUser &&
                              cmsUser.token &&
                              typeof cmsUser.token === 'string' &&
                              cmsUser.token.trim() !== '' &&
                              cmsUser.token !== '[]' &&
                              cmsUser.token !== 'undefined' &&
                              cmsUser.token !== 'null';

         // console.log("üîç [handleLoginPageLoad] Token CMS valide:", isValidCmsUser);
         if (cmsUser) {
             // console.log("üîç [handleLoginPageLoad] Token CMS details - token length:", cmsUser.token?.length, "id:", cmsUser.id);
         }

         // 3. Cas 1: Session Appwrite ET token CMS valide = utilisateur authentifi√©
         if (appwriteUser && isValidCmsUser) {
             // console.log("‚úÖ [handleLoginPageLoad] Authentification compl√®te - Appwrite + CMS");
             setAuthData(appwriteUser.email, appwriteUser.name, cmsUser);
             if (userEmailDisplay) userEmailDisplay.textContent = ` (${appwriteUser.email})`;
             showUIState('loggedIn');
             return;
         }

         // 4. Cas 2: Session Appwrite valide MAIS pas de token CMS (ou invalide)
         // ‚Üí Tenter de r√©cup√©rer un nouveau token CMS
         if (appwriteUser && !isValidCmsUser) {
             // console.log("‚ö†Ô∏è [handleLoginPageLoad] Session Appwrite OK mais token CMS manquant/invalide");
             try {
                 // console.log("üîÑ [handleLoginPageLoad] Tentative de r√©cup√©ration du token CMS...");
                 await setupCmsAuthentication();
                 const newCmsUser = getLocalCmsUser();
                 const isNewCmsUserValid = newCmsUser &&
                                         newCmsUser.token &&
                                         typeof newCmsUser.token === 'string' &&
                                         newCmsUser.token.trim() !== '' &&
                                         newCmsUser.token !== '[]';

                 if (isNewCmsUserValid) {
                     // console.log("‚úÖ [handleLoginPageLoad] Token CMS r√©cup√©r√© avec succ√®s");
                     setAuthData(appwriteUser.email, appwriteUser.name, newCmsUser);
                     if (userEmailDisplay) userEmailDisplay.textContent = ` (${appwriteUser.email})`;
                     showUIState('loggedIn');
                     return;
                 } else {
                     console.error("‚ùå [handleLoginPageLoad] √âchec de r√©cup√©ration du token CMS");
                     throw new Error("Impossible de r√©cup√©rer le token CMS");
                 }
             } catch (error) {
                 console.error("‚ùå [handleLoginPageLoad] Erreur lors de la r√©cup√©ration du token CMS:", error);

                 // Gestion sp√©cifique de l'erreur "email non v√©rifi√©"
                 if (error.message === "EMAIL_NOT_VERIFIED") {
                     console.warn("‚ö†Ô∏è [handleLoginPageLoad] Email non v√©rifi√© - affichage du message appropri√©");
                     // Ne pas nettoyer la session, l'utilisateur doit pouvoir v√©rifier son email
                     showUIState('emailNotVerified'); // Utiliser le nouvel √©tat sp√©cifique
                     return;
                 }

                 // Nettoyer la session Appwrite d√©faillante pour les autres erreurs
                 try {
                     await account.deleteSession('current');
                 } catch (cleanupError) {
                     console.warn("Erreur lors du nettoyage de la session Appwrite:", cleanupError);
                 }
                 clearAuthData();
                 showUIState('loggedOut');
                 return;
             }
         }

         // 5. Cas 3: Token CMS valide MAIS pas de session Appwrite
         // ‚Üí Le token CMS seul ne suffit pas, nettoyer
         if (!appwriteUser && isValidCmsUser) {
             // console.log("‚ö†Ô∏è [handleLoginPageLoad] Token CMS valide mais pas de session Appwrite - nettoyage");
             clearAuthData();
             showUIState('loggedOut');
             return;
         }

         // 6. Cas 4: Ni session Appwrite ni token CMS valide
         // console.log("‚ÑπÔ∏è [handleLoginPageLoad] Aucune authentification trouv√©e - affichage du formulaire");
         clearAuthData(); // Nettoyer au cas o√π
         showUIState('loggedOut');

     } catch (error) {
         console.error("‚ùå ERREUR CRITIQUE [handleLoginPageLoad]:", error.message);
         clearAuthData();
         showUIState('loggedOut');
     }
 }


